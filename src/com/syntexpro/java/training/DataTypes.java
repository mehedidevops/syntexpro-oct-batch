package com.syntexpro.java.training;

public class DataTypes {

    //2 types Data types/types

    //1 Primitive Data types:
    // i. byte      = num
    //ii. short     = num
    //iii. int      = num
    //iv. long      = num

    //v. float = decimal
    //vi. double = decimal

    //vii. boolean  = true/false
    //viii. char  = character



    //2 Non-primitive Data types
    // String, Object, Arrays, Class

    public static void main(String[] args) {

        byte myByte = 25;   //-128 to 127
        //System.out.println(myByte);

        short myShort = 32767;   // -32768 to 32767
        //System.out.println(myShort);

        int myInt = 25;  //**********
        System.out.println(myInt);

        long myLong = 123657556465656445L;
        System.out.println(myLong);

        float myFloat = 1.12345678901F;  // 7 digits
        System.out.println(myFloat);

        double myDouble = 1.12345678901234567890;  //*********** up to 16 digits
        System.out.println(myDouble);

        boolean myBoolean = false;  //********
        System.out.println(myBoolean);

        char myChar = '?';   //*****
        System.out.println(myChar);


        String myString = "% I am learning Java";
        System.out.println(myString);


    }




}
