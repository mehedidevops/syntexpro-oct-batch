package com.syntexpro.java.training;

public class SwitchAnother {

    public static void main(String[] args) {

        int a = 3;
        switch (a) {
            case 1:
                System.out.println("Light is on");
                break;
            case 2:
                System.out.println("Light id off");
                break;
            default:
                System.out.println("No light on the button");

        }
    }
}
